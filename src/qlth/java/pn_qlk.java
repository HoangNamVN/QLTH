    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (!checkNull()) {
            if (state == 3) {
                String sql = "update KhoiLop set TenKhoi = ? where MaKhoi =?";
                try {
                    pst = conn.prepareStatement(sql);
                    pst.setString(1, txtmakhoi.getText());
                    pst.setString(2, txtkhoi.getText());
                    pst.executeUpdate();
                    Update_table3("select * from KhoiLop");
                    setLuu(false);
                    JOptionPane.showMessageDialog(null, "Sửa thành công!");
                } catch (SQLException ex) {
                    Logger.getLogger(pn_qlk.class.getName()).log(Level.SEVERE, null, ex);
                }
                state = 0;
            }
            if (state == 1) {
                String sql = "insert into KhoiLop(MaKhoi,TenKhoi) values(?,?)";
                try {
                    pst = conn.prepareStatement(sql);
                    pst.setString(1, txtmakhoi.getText());
                    pst.setString(2, txtkhoi.getText());
                    pst.executeUpdate();
                    Update_table3("select * from KhoiLop");
                    setLuu(false);
                    JOptionPane.showMessageDialog(null, "Thêm thành công!");
                } catch (SQLException ex) {
                    Logger.getLogger(pn_qlk.class.getName()).log(Level.SEVERE, null, ex);
                }
                state = 0;
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

